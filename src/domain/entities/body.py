# coding utf-8

from typing import Annotated

from pydantic import Field

from .base import ISchema

from .typing import (
    TModel,
    TQuality,
)


class IBody(ISchema):
    """Базовое тело запроса для конфигурации AI-модели.

    Содержит обязательные параметры для работы с AI-моделью:
    - Выбор конкретной модели
    - Длительность обработки
    - Текст запроса (промпт)
    - Качество результата

    Наследует все особенности сериализации от ISchema.
    """

    model: Annotated[
        TModel,
        Field(...),
    ]
    """Выбор конкретной AI-модели для обработки запроса.
    
    Тип:
        TModel: Перечисление доступных моделей
    Обязательное поле.
    """

    duration: Annotated[
        int,
        Field(...),
    ]
    """Длительность обработки запроса в миллисекундах.
    
    Тип:
        int: Положительное целое число
    Обязательное поле.
    """

    prompt: Annotated[
        str,
        Field(...),
    ]
    """Текст запроса (промпт) для обработки AI-моделью.
    
    Тип:
        str: Непустая строка
    Обязательное поле.
    """

    quality: Annotated[
        TQuality,
        Field(...),
    ]
    """Уровень качества ожидаемого результата.
    
    Тип:
        TQuality: Перечисление возможных уровней качества
    Обязательное поле.
    """
